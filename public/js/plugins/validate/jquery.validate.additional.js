jQuery.validator.addMethod("ipsegs_n", function(value, element) {
  function testIP(address){
    return this.optional(element) || /^(25[0-5]|2[0-4]\d|[01]?\d\d?)\.(25[0-5]|2[0-4]\d|[01]?\d\d?)\.(25[0-5]|2[0-4]\d|[01]?\d\d?)\.(25[0-5]|2[0-4]\d|[01]?\d\d?)$/i.test(address);
  }
  var ips = value.split("\n");
  for(var i=0; i<ips.length; i++){
    var ip = ips[i].trim();
    if(ip === "") continue;


    if(ip.indexOf("-") < 0 && !testIP.call(this, ip.trim())){
      return false;
    } else if(ip.indexOf("-") >= 0) {
      var parts = ip.split("-");
      if(parts.length != 2 || !testIP.call(this, parts[0]) || !testIP.call(this, parts[1])) {
        return false;
      }
    }
  }
  return true;
}, "Please enter a valid IP v4 address.");


